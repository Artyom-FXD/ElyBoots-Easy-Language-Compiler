#ifndef ELY_GC_H
#define ELY_GC_H

#include <stddef.h>
#include <stdint.h>

// ============================================================================
// Параметры сборщика (настраиваются)
// ============================================================================
// Размер молодого поколения (байт)                     // ~ 2 строки
#define YOUNG_SIZE (2 * 1024 * 1024)   // 2 MB

// Размер старого поколения (байт)                       // ~ 2 строки
#define OLD_SIZE   (8 * 1024 * 1024)   // 8 MB

// Максимальное количество сборок молодого поколения    // ~ 2 строки
// после которых объект продвигается в старое
#define PROMOTION_AGE 2

// ============================================================================
// Типы объектов, которые умеет обходить GC
// ============================================================================
typedef enum {
    GC_OBJ_VALUE,   // ely_value*
    GC_OBJ_ARR,     // arr*
    GC_OBJ_DICT,    // dict*
    GC_OBJ_STRING   // char* (не содержит ссылок)
} gc_obj_type_t;

// ============================================================================
// Заголовок GC, размещается перед каждым выделенным блоком
// ============================================================================
typedef struct gc_header {
    struct gc_header* next;          // следующий в списке всех объектов
    uint32_t size;                   // размер блока (включая заголовок)
    uint16_t age;                    // сколько сборок молодого поколения пережил
    uint8_t flags;                   // битовые флаги (marked, in_old, dirty)
    uint8_t obj_type;                // gc_obj_type_t
    struct gc_header* forwarding;    // используется при копировании/компактизации
} gc_header_t;

// ============================================================================
// Инициализация и основная сборка
// ============================================================================

// Инициализирует все структуры GC, выделяет начальные пулы памяти.
// Вызывается один раз при запуске программы (до main).
// Размер: ~50 строк (выделение памяти под поколения, корни, списки)
void gc_init(void);

// Запускает полную сборку мусора (молодое + старое поколения).
// Обычно вызывается автоматически при нехватке памяти, но может быть вызвана принудительно.
// Размер: ~80 строк (управление поколениями, вызов young/old collection)
void gc_collect(void);

// ============================================================================
// Выделение памяти
// ============================================================================

// Выделяет блок памяти под объект заданного типа. Все новые объекты попадают в молодое поколение.
// Возвращает указатель на начало полезных данных (без заголовка).
// Размер: ~40 строк (выбор поколения, выравнивание, размещение заголовка, проверка места)
void* gc_alloc(size_t size, gc_obj_type_t type);

// ============================================================================
// Управление корнями (roots)
// ============================================================================

// Регистрирует указатель на корневой объект (глобальная переменная или поле стека).
// Во время сборки GC будет обходить все зарегистрированные корни.
// Размер: ~15 строк (расширение массива корней, добавление)
void gc_add_root(void** root_ptr);

// Удаляет ранее зарегистрированный корень. Вызывается при выходе переменной из области видимости.
// Размер: ~15 строк (поиск и удаление из массива корней)
void gc_remove_root(void** root_ptr);

// Регистрирует глобальный корень (не удаляется до конца программы). Добавляется один раз.
// Размер: ~15 строк
void gc_add_global_root(void** root_ptr);

// ============================================================================
// Write barrier (барьер записи) для отслеживания ссылок из старого поколения в молодое
// ============================================================================

// Вызывается перед присваиванием *field_ptr = new_value, когда parent_obj находится в старом поколении.
// Помечает родительский объект как "грязный", чтобы при сборке молодого поколения сканировать его.
// Размер: ~20 строк (проверка поколений, пометка dirty, добавление в список dirty)
void gc_write_barrier(void* parent_obj, void** field_ptr, void* new_value);

// ============================================================================
// Функции для отладки и статистики
// ============================================================================

// Печатает статистику использования памяти: размеры поколений, количество объектов, число сборок.
// Размер: ~30 строк
void gc_dump_stats(void);

// Возвращает общий размер выделенной GC-памяти (молодое + старое поколения).
// Размер: ~5 строк
size_t gc_get_heap_size(void);

// Возвращает текущий объём свободной памяти в куче GC.
// Размер: ~5 строк
size_t gc_get_free_size(void);

void gc_enable(void);
void gc_disable(void);

#endif // ELY_GC_H